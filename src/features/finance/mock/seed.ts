const daysAgo = (days: number, hours = 10) => {
  const d = new Date();
  d.setDate(d.getDate() - days);
  d.setHours(hours, 0, 0, 0);
  return d.toISOString();
};

const todayAt = (hours = 11) => {
  const d = new Date();
  d.setHours(hours, 0, 0, 0);
  return d.toISOString();
};

function calcGstSummary(products: FinanceInvoiceProduct[]): FinanceGstSummary {
  let taxableAmount = 0;
  let totalGst = 0;

  for (const p of products) {
    taxableAmount += p.amount;
    totalGst += p.amount * (p.gstRate / 100);
  }

  return {
    taxableAmount,
    cgst: totalGst / 2,
    sgst: totalGst / 2,
    igst: 0,
    totalGst,
  };
}

function calcOrderSummary(
  products: FinanceInvoiceProduct[],
  discount = 0,
  deliveryCharges = 0,
): FinanceOrderSummary {
  const subtotal = products.reduce((sum, p) => sum + p.amount, 0);
  const gst = calcGstSummary(products);
  return {
    subtotal,
    discount,
    deliveryCharges,
    gstTotal: gst.totalGst,
    grandTotal: subtotal - discount + deliveryCharges + gst.totalGst,
  };
}

function buildTimeline(
  status: FinancePaymentStatus,
  orderCreatedAt: string,
  invoiceDate: string,
  paymentDate?: string,
  verifiedAt?: string,
): FinanceTimelineEvent[] {
  const steps: FinanceTimelineEvent[] = [
    { step: "ORDER_CREATED", label: "Order Created" },
    { step: "INVOICE_GENERATED", label: "Invoice Generated" },
    { step: "PAYMENT_PENDING", label: "Payment Pending" },
    { step: "PAYMENT_RECEIVED", label: "Payment Received" },
    { step: "VERIFIED", label: "Verified" },
  ];

  steps[0].completedAt = orderCreatedAt;
  steps[1].completedAt = invoiceDate;

  if (status === "Pending") {
    steps[2].completedAt = invoiceDate;
    return steps;
  }

  if (status === "Paid") {
    steps[2].completedAt = invoiceDate;
    steps[3].completedAt = paymentDate;
    steps[4].completedAt = verifiedAt ?? paymentDate;
    return steps;
  }

  steps[2].completedAt = invoiceDate;
  return steps;
}

function createInvoice(
  data: Omit<
    FinanceInvoice,
    "gstSummary" | "orderSummary" | "timeline" | "invoiceAmount"
  > & { discount?: number; deliveryCharges?: number },
): FinanceInvoice {
  const gstSummary = calcGstSummary(data.products);
  const orderSummary = calcOrderSummary(
    data.products,
    data.discount,
    data.deliveryCharges,
  );

  return {
    ...data,
    gstSummary,
    orderSummary,
    invoiceAmount: orderSummary.grandTotal,
    timeline: buildTimeline(
      data.status,
      data.orderCreatedAt,
      data.invoiceDate,
      data.paymentDate,
      data.verifiedAt,
    ),
  };
}

